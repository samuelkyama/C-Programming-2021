// working of relational operators
#include <stdio.h>

int main()
{
	int a=5,b=10;
	printf("Return value of(%d==%d)is %d\n",a,b,a==b);
	printf("Return value of (%d!=%d)is %d\n",a,b,a!=b);
	printf("Return value of (%d<%d)is %d\n",a,b,a<b);
	printf("Return value of (%d<=%d)is %d\n",a,b,a<=b);
	printf("Return value of (%d>%d)is %d\n",a,b,a>b);
	printf("Return value of (%d>=%d)is %d\n",a,b,a>=b);
	return 0;
	}
